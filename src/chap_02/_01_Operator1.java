package chap_02;

public class _01_Operator1 {
    public static void main(String[] args) {
        System.out.println(4+2);
        System.out.println(4-2);

        //++ --
        int val;
        val = 10;
        System.out.println(val); //10
        System.out.println(++val); //11
        System.out.println(val++);
        System.out.println(val);
    }
}
